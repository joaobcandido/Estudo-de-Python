# para utilizar a estrutura condicional if fazemos assim
numero = 21 # declaracao de uma variavel

if numero == 1: # se numero for igual a 1 entao :
    print("numero eh igual a 1") # escreva na tela numero eh igual a 1
elif numero == 2: # se numero for igual a 2 entao :
    print("numero eh igual a 2") # escreva na tela numero eh igual a 2
else:
    print("numero nao eh 1 nem 2") # escreva na tela numero nao eh 1 nem 2   